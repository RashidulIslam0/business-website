const mongoose = require('mongoose');

// Define the Mongoose schema
const bookSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  startTime: {
    type: String,
    required: true,
  },
  endTime: {
    type: String,
    required: true,
  },
},{timestamps:true});

// Create a Mongoose model

const Book = mongoose.model("book", bookSchema);
module.exports = Book;
